     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {

        
        $request->validate([
            'refer'=>'required',
            'desig'=>'required',
            'categ'=>'required',
            'img'=>'required|mimes:jpg,png,jpeg'
        ]);

        
        $newImage=$request->desig . '.' .$request->img->extension();
        $request->img->move(public_path('img'),$newImage);
        

        $produit=Produit::create([
            'ref_produit'=>$request->input('refer'),
            'design_produit'=>$request->input('desig'),
            'image'=>$newImage,
            'categorie_id'=>$request->input('categ'),
        ]);
        
        return back()->with('msg','Produit ajouter avec succés !');
    }

